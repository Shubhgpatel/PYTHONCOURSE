name = "Shubh g Patel"

name2 = name[0:5] #this is called string slicing and the last index is exclusive
print(name)
print(name2)
char1 = name[0]
print(char1) # this will print the first character of the string
char2 = name[-1]
print(char2) # this will print the last character of the string
name3 = name[:] # this will print the whole string
print(name3)
name4 = name[0:] # this will print the whole string
print(name4)
