# Object Oriented Programming in Python.

class Employee: # its a very simple example of class
    age = 20  # this is a class attirbute
    language = "Py"

e1 = Employee() # Object creation 
e1.name = "Shubh" # this is an instance attribute
print(e1.name,e1.age,e1.language)

# here name is instance attribute and age , language are class attribute as they directly belongs to the class.