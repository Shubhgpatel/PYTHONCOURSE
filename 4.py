# string functions

name = "Shubh g Patel"
print(len(name)) # len function is used to get the length of the string
print(name.endswith("tel")) # this will return true if the string ends with the given string
print(name.startswith("Shu")) # this will return true if the string starts with the given string
print(name.find("g")) # this will return the index of the given string


